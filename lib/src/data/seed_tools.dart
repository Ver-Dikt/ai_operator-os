import '../models/ai_tool.dart';

AiTool _tool({
  required String id,
  required String name,
  required ToolCategory category,
  required String url,
  required PricingType pricing,
  required String bestFor,
  required List<String> tags,
  bool hasApi = false,
  String? apiUrl,
  String free = 'Check current plan before production use.',
  String limitations = 'Limits and pricing can change. Verify before launch.',
  List<ToolPlatform> platforms = const [ToolPlatform.web],
  double rating = 4.5,
  List<String> useCases = const [],
  List<String> agents = const [],
  List<String> workflows = const [],
  List<String> alternatives = const [],
  ToolIntegrationType? integration,
}) {
  return AiTool(
    id: id,
    name: name,
    category: category,
    description:
        '$name helps with ${category.label.toLowerCase()} tasks: $bestFor.',
    url: url,
    pricingType: pricing,
    hasApi: hasApi,
    apiUrl: apiUrl,
    freeCreditsInfo: free,
    bestFor: bestFor,
    limitations: limitations,
    tags: tags,
    rating: rating,
    platforms: platforms,
    recommendedUseCases: tags,
    workflowExamples: ['Tool routing', 'Prompt pack', 'Production pipeline'],
    categoryIds: [category.name],
    useCaseIds: useCases,
    agentIds: agents,
    workflowIds: workflows,
    alternativeToolIds: alternatives,
    integrationType:
        integration ??
        (platforms.contains(ToolPlatform.local)
            ? ToolIntegrationType.localLater
            : hasApi
            ? ToolIntegrationType.apiLater
            : ToolIntegrationType.externalManual),
  );
}

